// C# program to demonstrate the use of
// the string concatenation operator.
using System;
class String3 {
	// Main Method
	public static void Main()
	{
		string s1 = "HTML";
		string s2 = "CSS";
		string s3 = "JS";
		string s4 = "Csharp";
		// using concatenation operator
		string str = s1 + s2 + s3 + s4 + "Imp";
		Console.WriteLine(str);
	}
}